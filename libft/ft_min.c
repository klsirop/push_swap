char	ft_min(char *arr)
{
	int		i;
	char	min;

	if (arr)
	{
		i = 0;
		while (arr[i] != '\0')
		{
			if (i == 0)
			{
				min = arr[i];
			}
			else
			{
				if (arr[i] < min)
					min = arr[i];
			}
			i++;
		}
		if ((arr[i] == '\0') && (i == 0))
			return ('0');
		return (min);
	}
	return ('0');
}
