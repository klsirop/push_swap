char	ft_max(char *arr)
{
	int		i;
	char	max;

	if (arr)
	{
		i = 0;
		while (arr[i] != '\0')
		{
			if (i == 0)
				max = arr[i];
			else
			{
				if (arr[i] > max)
					max = arr[i];
			}
			i++;
		}
		if ((i == 0) && (arr[i] == '\0'))
			return ('0');
		return (max);
	}
	return ('0');
}
