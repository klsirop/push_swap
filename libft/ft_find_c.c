int		ft_find_c(const char *str, char c)
{
	int		n;
	int		i;

	n = -1;
	if (str)
	{
		i = 0;
		n = 0;
		while ((char)str[i] != '\0')
		{
			if ((char)str[i] == c)
				n++;
			i++;
		}
		if (c == '\0')
			n++;
	}
	return (n);
}
